                            <h5>Sampah Anorganik</h5>
                            <p><strong>Pengertian:</strong> Sampah anorganik adalah jenis sampah yang tidak dapat terurai secara hayati dan membutuhkan waktu yang lama untuk terdegradasi.</p>
                            <p><strong>Contoh:</strong> Plastik, kaca, logam, dan bahan-bahan sintetis lainnya.</p>
                            <p><strong>Manfaat Pengelolaan:</strong> Sampah anorganik dapat didaur ulang menjadi bahan baku untuk produk baru, sehingga mengurangi kebutuhan akan sumber daya alam baru.</p>
